//Laços de Repetição

//While
let count = 1;
while (count <= 100) {
    console.log (count);
    count ++;
}

//---------------------

//DoWhile
let count2 = 1;
do {
    console.log (count2);
    count2 ++;
} while (count2 <= 100);

//---------------------

//For
for (let i = 1; i <= 100; i++) {
    console.log(i);
}

